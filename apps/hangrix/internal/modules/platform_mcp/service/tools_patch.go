// issuePatchApplyResultTool is called by the apply agent after it attempts
// to `git am` the patch series in its workspace. It writes back success
// (with the new commit SHA) or failure (with an error message).
func (r *Registry) issuePatchApplyResultTool() *platformmcpdomain.Tool {
	return &platformmcpdomain.Tool{
		Name:        "issue_patch_apply_result",
		Description: "Report the result of a patch application attempt. Called by the apply agent after `git am` + `git push`. On success the patch is marked 'applied' and the issue head is updated. On failure the error is recorded and the patch can be retried.",
		InputSchema: map[string]any{
			"type": "object",
			"properties": map[string]any{
				"submission_id": map[string]any{
					"type":        "integer",
					"description": "Patch submission ID that was being applied.",
				},
				"success": map[string]any{
					"type":        "boolean",
					"description": "Whether `git am` + `git push` succeeded.",
				},
				"commit_sha": map[string]any{
					"type":        "string",
					"description": "The new commit SHA on the issue branch (required when success=true).",
				},
				"error": map[string]any{
					"type":        "string",
					"description": "Error message from `git am` or `git push` (required when success=false).",
				},
			},
			"required": []string{"submission_id", "success"},
		},
		Call: func(ctx context.Context, sess *runnerdomain.AgentSession, args json.RawMessage) (platformmcpdomain.Result, error) {
			scope, err := r.loadScope(ctx, sess)
			if err != nil {
				return errorResult(err.Error()), nil
			}
			var req struct {
				SubmissionID int64  `json:"submission_id"`
				Success      bool   `json:"success"`
				CommitSHA    string `json:"commit_sha"`
				Error        string `json:"error"`
			}
			if err := unmarshalArgs(args, &req); err != nil {
				return errorResult("invalid arguments: " + err.Error()), nil
			}
			if req.SubmissionID <= 0 {
				return errorResult("submission_id is required and must be positive"), nil
			}

			patch, err := r.deps.Patches.GetPatch(ctx, req.SubmissionID)
			if err != nil {
				return errorResult("get patch: " + err.Error()), nil
			}
			if patch.IssueID != scope.issue.ID {
				return errorResult("patch does not belong to the current issue"), nil
			}
			if patch.Status != issuedomain.PatchStatusApplying {
				return errorResult(fmt.Sprintf("patch is not 'applying' (current: '%s')", patch.Status)), nil
			}

			if req.Success {
				commitSHA := strings.TrimSpace(req.CommitSHA)
				if commitSHA == "" {
					return errorResult("commit_sha is required when success=true"), nil
				}
				updated, err := r.deps.Patches.UpdatePatchStatus(ctx, patch.ID, issuedomain.PatchStatusApplied, commitSHA, "")
				if err != nil {
					return errorResult("update patch status: " + err.Error()), nil
				}
				// Update issue head SHA.
				_ = r.deps.Issues.UpdateHeadSHA(ctx, scope.issue.ID, commitSHA)

				// Write timeline event.
				payload, _ := json.Marshal(issuedomain.PatchEventPayload{
					SubmissionID: updated.ID,
					Title:        updated.Title,
					AgentRole:    updated.AgentRole,
					CommitSHA:    commitSHA,
				})
				_, _ = r.deps.Issues.CreateAgentEvent(ctx, scope.issue.ID, issuedomain.EventPatchApplied, payload, sess.RoleKey)

				return textResult(map[string]any{
					"id":          updated.ID,
					"status":      string(updated.Status),
					"commit_sha":  commitSHA,
					"applied_at":  stableTime(*updated.AppliedAt),
				}), nil
			}

			// Failure: record the error but leave status as 'applying' so it
			// can be retried. Use UpdateApplyError for the error field.
			applyErr := strings.TrimSpace(req.Error)
			if applyErr == "" {
				applyErr = "unknown apply error"
			}
			if err := r.deps.Patches.UpdateApplyError(ctx, patch.ID, applyErr); err != nil {
				return errorResult("update apply error: " + err.Error()), nil
			}
			// Re-fetch to get updated state.
			updated, _ := r.deps.Patches.GetPatch(ctx, patch.ID)

			return textResult(map[string]any{
				"id":     updated.ID,
				"status": string(updated.Status),
				"error":  updated.ApplyError,
			}), nil
		},
	}
}

// parsePatchStats extracts file stats from a unified diff. Returns
// changed_paths, file_count, additions, deletions. Best-effort: on
// parse failure returns zero values so the patch is still recorded.
func parsePatchStats(patch string) (paths []string, fileCount, additions, deletions int32) {
	lines := strings.Split(patch, "\n")
	seenPaths := make(map[string]struct{})
	for _, line := range lines {
		if strings.HasPrefix(line, "+++ ") || strings.HasPrefix(line, "--- ") {
			// Extract filename from "+++ b/path" or "--- a/path"
			parts := strings.SplitN(line, "/", 2)
			if len(parts) == 2 {
				p := parts[1]
				if p != "" && p != "/dev/null" {
					if _, ok := seenPaths[p]; !ok {
						seenPaths[p] = struct{}{}
						paths = append(paths, p)
					}
				}
			}
		}
		if strings.HasPrefix(line, "+") && !strings.HasPrefix(line, "+++") {
			additions++
		}
		if strings.HasPrefix(line, "-") && !strings.HasPrefix(line, "---") {
			deletions++
		}
	}
	fileCount = int32(len(paths))
	return
}

// resolveWorkspacePath validates that the given path is a workspace-relative
// path (not absolute) and resolves it to an absolute path anchored to the
// workspace root. Returns an error if the path is absolute, attempts to
// escape the workspace root via "..", or cannot be resolved.
func resolveWorkspacePath(workspaceRoot, p string) (string, error) {
	p = filepath.Clean(p)
	if filepath.IsAbs(p) {
		return "", fmt.Errorf("absolute paths are not allowed")
	}
	resolved := filepath.Join(workspaceRoot, p)
	// filepath.Join + Clean handles ".." but we double-check we're still
	// inside the workspace root (no symlink traversal here; symlinks
	// require platform-level controls or os.Readdir).
	rel, err := filepath.Rel(workspaceRoot, resolved)
	if err != nil || rel == ".." || strings.HasPrefix(rel, ".."+string(filepath.Separator)) {
		return "", fmt.Errorf("path escapes workspace root")
	}
	return resolved, nil
}


